@dataclass
class Location:
    location_id: int
    diff: int
    area: int
    max_size: int
    is_transient: bool = False
    is_race_key_loc: bool = False
    linked_locations: list[int] = dc_field(default_factory=list)
    has_flag: int = -1
    has_cumul_flag: object = None
    default_key: object = None

    def __post_init__(self):
        self.validate()

    def validate(self):
        if self.diff == LOC_DIF.IGNORE and self.area != AREA.NONE:
            print("Warning: Difficulty = IGNORE and area != NONE")
        if (
            self.diff
            not in {
                LOC_DIF.NPC_EASY,
                LOC_DIF.NPC_MEDIUM,
                LOC_DIF.NPC_HARD,
                LOC_DIF.EMPTY,
                LOC_DIF.UPGRADE,
                LOC_DIF.RANDOM_UPGRADE,
            }
            and self.area == AREA.NPC_RNG_DROP
        ):
            print(
                "Warning: Difficulty != NPC_(EASY|MEDIUM|HARD) "
                "but area = NPC_RNG_DROP"
            )
        if (
            self.diff in {LOC_DIF.NPC_EASY, LOC_DIF.NPC_MEDIUM, LOC_DIF.NPC_HARD}
            and self.area != AREA.NPC_RNG_DROP
        ):
            print(
                "Warning: Difficulty = NPC_(EASY|MEDIUM|HARD) and area != NPC_RNG_DROP"
            )

    @property
    def better_repr(self):
        """
        Special repr used to get names of IntEnums instead of values/reprs
        """

        field_list = fields(self)
        attrib_list = []

        for f in field_list:
            attr = getattr(self, f.name)
            if attr == f.default or (f.name == "linked_locations" and attr == []):
                continue
            if isinstance(attr, IntEnum):
                attrib_list.append(f"{f.name}={type(attr).__name__}.{attr.name}")
            else:
                attrib_list.append(f"{f.name}={attr!r}")

        args = ", ".join(attrib_list)

        return f"{self.__class__.__name__}({args})"

    def __str__(self):
        return f"Location (id: {self.location_id})"


